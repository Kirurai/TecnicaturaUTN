
import java.util.ArrayList;
import java.util.List;

/**
 *
 * @author SkylakeFrost
 */
public class OrdenPedido {

    private String destino;
    private String origen;
    private List<Producto> reponerProductos = new ArrayList<>();

    public OrdenPedido(String destino, String origen) {
        this.destino = destino;
        this.origen = origen;

    }

    public OrdenPedido() {
    }

    public String getDestino() {
        return destino;
    }

    public void setDestino(String destino) {
        this.destino = destino;
    }

    public String getOrigen() {
        return origen;
    }

    public void setOrigen(String origen) {
        this.origen = origen;
    }

    public List<Producto> getReponerProductos() {
        return reponerProductos;
    }

    public void setReponerProductos(List<Producto> reponerProductos) {
        this.reponerProductos = reponerProductos;
    }

}
